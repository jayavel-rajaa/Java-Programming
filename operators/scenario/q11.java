/*A shopkeeper sells an item at a profit.
Given cost price and profit, calculate the selling price using:
SP = CP + Profit.*/
import java.util.Scanner;
public class q11 { 
public static void main(String[] args){
    Scanner sc=new Scanner(System.in);
    int a=sc.nextInt();
    int b=sc.nextInt();
    int tot=a+b;
    System.out.println("selling price: "+tot);
}
}