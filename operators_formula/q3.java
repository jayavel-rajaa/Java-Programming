//Q3.Write a Java Program to find area of Triangle
import java.util.Scanner;
public class q3 { 
public static void main(String[] args){
    Scanner sc=new Scanner(System.in);
    int l=sc.nextInt();
    int b=sc.nextInt();
    System.out.println("Area of Triangle: "+(0.5)*(l*b));     
}
}